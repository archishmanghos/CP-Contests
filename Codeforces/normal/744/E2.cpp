template <typename num_t>
using ordered_set = tree<num_t, null_type, less_equal<num_t>, rb_tree_tag, tree_order_statistics_node_update>;

int T, N, A;

void solve()
{
    cin >> N;
    int ans = 0;
    ordered_set<int> os_inc, os_dec;

    for(int i = 1; i <= N; i++)
    {
    	cin >> A;
    	int left = os_inc.order_of_key(A);
    	int right = os_dec.order_of_key(-A);
    	ans += min(left, right);
    	os_inc.insert(A);
    	os_dec.insert(-A);
    }

   	cout << ans << '\n';
}

