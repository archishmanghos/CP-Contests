int T;
string S1, S2;

void solve()
{
    cin >> S1 >> S2;
    int N1 = S1.size(), N2 = S2.size();

    string T = S1;
    sort(T.begin(), T.end());

    if(T >= S2)
    {
        cout << "---" << '\n';
        return;
    }

    for(int i = 0; i < N1; i++)
    {
        if(S1[i] == T[i])
            continue;
        for(int j = i + 1; j < N1; j++)
        {
            if(S1[j] == T[i])
            {
                swap(S1[j], S1[i]);
                if(S1 < S2)
                {
                    cout << S1 << '\n';
                    return;
                }
                swap(S1[j], S1[i]);
            }
        }
    }

    if(S1 < S2)
        cout << S1 << '\n';
    else
        cout << "---" << '\n';
}

