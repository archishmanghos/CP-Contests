struct point
{
	int x, y;
};

int P, Q;
point interval, A[100], B[100];

int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

	#ifndef ONLINE_JUDGE
	    freopen("error.txt", "w", stderr);
	#endif

	cin >> P >> Q >> interval.x >> interval.y;
	for(int i = 1; i <= P; i++)
		cin >> A[i].x >> A[i].y;
	for(int i = 1; i <= Q; i++)
		cin >> B[i].x >> B[i].y;

	int ans = 0;
	for(int i = interval.x; i <= interval.y; i++)
	{
		bool match = false;
		for(int j = 1; j <= Q; j++)
		{
			point check;
			check.x = B[j].x + i;
			check.y = B[j].y + i;
			
			for(int k = 1; k <= P; k++)
			{
				if(check.y < A[k].x || check.x > A[k].y)
					continue;
				match = true;
				if(match)
					break;
			}
			
			if(match)
				break;
		}

		ans += match == true;
	}

	cout << ans;

    return 0;
}